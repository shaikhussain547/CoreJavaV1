package javaBasics;

public class DataTypes {

	public static void main(String[] args) {
		//DataTypes
		//1.int DataType
		int i = 10;
		System.out.println(i);
		i = -2;
		i = 0;
		//2.double DataType - float value
		double j = 12.33;
		j = 100; //100.00
		double num;
		//3.char DataType - Any character
		char k = 'H';
		k = '%';
		k = '1';
		//4.boolean DataType - True/False
		boolean b1 = true;
		boolean b2 = false;
		//5.String is class, not a data type
		String s = "Selenium";
		s = "1000";
		s = "12.33";
		//output screen is also known as console.
	}

}
