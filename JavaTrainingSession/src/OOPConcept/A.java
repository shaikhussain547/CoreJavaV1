package OOPConcept;
//Super keyword
public class A {
	
	public A() {
		System.out.println("parent class const");
	}
	
	public A(int i) {
		System.out.println("Parent class const with value of i "+i);
	}
	
	public A(int i, int j) {
		System.out.println("Parent class const with value of i and j = "+i+" and "+j);
	}
	
}
